public class Swap
{
	public static void main(String[] args)
	{
		int x=23, y=54,temp;
		System.out.println("Before Swapping of two Numbers");
		System.out.println("x:"+x);
		System.out.println("y:"+y);
		temp=x;
		x=y;
		y=temp;
		System.out.println("After Swapping of two Numbers");
		System.out.println("x:"+x);
		System.out.println("y:"+y);
	}
}
